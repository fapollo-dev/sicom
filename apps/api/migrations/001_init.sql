CREATE SEQUENCE IF NOT EXISTS seq_bancos_codbco;

CREATE TABLE IF NOT EXISTS bancos (
  codbco            integer PRIMARY KEY DEFAULT nextval('seq_bancos_codbco'),
  agencia           varchar(10),
  banco             varchar(50) NOT NULL,
  cidade            varchar(50) NOT NULL,
  uf                char(2),
  usultalteracao    integer,
  dtultimalteracao  timestamptz,
  dtcadastro        timestamptz,
  agencia_cedente   integer,
  codbcoblt         integer,
  convenio          integer,
  carteira_cobranca integer,
  variacao_carteira integer
);
ALTER SEQUENCE seq_bancos_codbco OWNED BY bancos.codbco;

-- View de pesquisa GET_BANCOS (aliases idĂªnticos ao legado: codigo, codigo_banco).
CREATE OR REPLACE VIEW get_bancos AS
SELECT banco, agencia, cidade,
       codbco    AS codigo,
       codbcoblt AS codigo_banco,
       convenio, carteira_cobranca, variacao_carteira
FROM bancos;

